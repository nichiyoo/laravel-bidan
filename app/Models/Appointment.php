<?php

namespace App\Models;

use App\Casts\TimeCast;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Relations\BelongsTo;
use Illuminate\Database\Eloquent\Relations\HasOne;

class Appointment extends Model
{
    use HasFactory;

    /**
     * The attributes that are not mass assignable.
     *
     * @var array<int, string>
     */
    protected $guarded = [];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'date' => 'datetime:Y-m-d',
        'time' => TimeCast::class,
    ];

    /**
     * Relationship between appointment and patient.
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
    public function patient(): BelongsTo
    {
        return $this->belongsTo(Patient::class);
    }

    /**
     * Relationship between appointment and payment.
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
    public function payment(): BelongsTo
    {
        return $this->belongsTo(Payment::class);
    }

    /**
     * Relationship between appointment and service.
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
    public function service(): BelongsTo
    {
        return $this->belongsTo(Service::class);
    }

    /**
     * Relationship betwwen appointment and notification
     * 
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function notification(): HasOne
    {
        return $this->hasOne(Notification::class);
    }

    /**
     * Relationship between appointment and diagnosis.
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function diagnosis(): HasOne
    {
        return $this->hasOne(Diagnosis::class);
    }
}
